#include "3-calc.h"

/**
 * op_add - Operation Addition
 * @a: num1
 * @b: num2
 *
 * Return: a + b
 */
int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - Operation Subtraction
 * @a: num1
 * @b: num2
 *
 * Return: a - b
 */
int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - Operation Multiplication
 * @a: num1
 * @b: num2
 *
 * Return: a * b
 */
int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - Operation Division
 * @a: num1
 * @b: num2
 *
 * Return: a / b
 */
int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - Operation Modulo
 * @a: num1
 * @b: num2
 *
 * Return: a % b
 */
int op_mod(int a, int b)
{
	return (a % b);
}
